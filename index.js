//movement control
function control(e) {
    if (e.defaultPrevented) {
        return;
    }

    switch(e.code) {
        case 'ArrowDown':
            console.log('down');
            break;
        case 'ArrowUp':
            console.log('up');
            break;
        case 'ArrowLeft':
            console.log('left');
            break;
        case 'ArrowRight':
            console.log('right');
            break;
    }
    e.preventDefault();
}
document.addEventListener('keydown', control);